package ua.org.smit.amvsampler.messages;

/**
 *
 * @author smit
 */
public class Message {

    private final Type type;
    private final String text;

    public Message(Type type, String text) {
        this.type = type;
        this.text = text;
    }

    public Type getType() {
        return type;
    }

    public String getText() {
        return text;
    }

}
